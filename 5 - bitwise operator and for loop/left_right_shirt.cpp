#include <iostream>
using namespace std;

int main(){

    cout << "Left shift by 2 " << (21<<2) << endl;
    cout << "Left shift by 1 " << (21<<1) << endl;

    cout << "Right shift by 2 " << (17>>2) << endl;
    cout << "Right shift by 1 " << (17>>1) << endl;

}


// 20 = 10100 (binary)

// 20 >> 1 → 01010 = 10     means n devided by 2
// 20 >> 2 → 00101 = 5      means n devided by 4